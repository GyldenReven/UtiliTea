function morseToText(morseCode) {
    return morseCode
        .split(' ')
        .map((morseChar) => {
            return Object.keys(morseArray).find((key) => morseArray[key] === morseChar);
        })
        .join('');
}

function textToMorse(text) {
    return text
        .split('')
        .map((char) => morseArray[char.toUpperCase()])
        .join(' ');
}

